<h1>Ranzgruppe!</h1>

<p>Willkommen bei Ranzgruppe, der bald größten Punkband im bekannten Universum!</p>

<h2>Was ist Ranzgruppe?</h2>

<p>Ranzgruppe ist eine Punk-Band aus Peine, Niedersachsen. Fast jeder kann mitmachen solange er ein Instrument spielen kann.</p>
<p>Die Band tritt niemals komplett auf, wir sind ja kein Orchester. Dafür können mehrere Gigs synchron stattfinden.</p>

<h2>Wer ist Ranzgruppe?</h2>

<p>Ranzgruppe wurde im August 2019 inoffiziell gegründet.</p>
<p>Die Gründer, gizmore(vocals) und lazer(Schädelklatscher) haben einige Songs &quot;komponiert&quot;, hatten allerdings noch nie einen Auftritt.</p>
